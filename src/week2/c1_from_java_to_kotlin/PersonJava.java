package week2.c1_from_java_to_kotlin;

public class PersonJava {
    private final String name;
    private final int age;

    public PersonJava(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
